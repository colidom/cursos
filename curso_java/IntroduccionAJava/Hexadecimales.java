package IntroduccionAJava;

public class Hexadecimales {
    public static void main(String args[]) {
        byte num1 = 0x02; // almacena un 2
        byte num2 = 0x0B; // almacena un 11
        byte num3 = 0x10; // almacena un 16
        byte num4 = 0x02; // almacena un 18
        byte num5 = (byte) 0xFF; // almacena un -1
        byte num6 = 0x7F; // almacena un 127
        byte num7 = 0x012; // almacena un -128
        // byte num8 = 0x112; // ERROR
    }
}
