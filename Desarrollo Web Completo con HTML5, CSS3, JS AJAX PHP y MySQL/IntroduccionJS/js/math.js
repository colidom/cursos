let result;

result = Math.PI;
result = Math.round(2.5); // Redondea
result = Math.ceil(2.5); // Redondea siempre hacia arriba
result = Math.floor(2.5); // Redondea siempre hacia abajo
result = Math.sqrt(2.5); // Raiz cuadrada
result = Math.abs(-2.5); // Convierne un númpero negativo en positivo
result = Math.min(2.5, 10, 1000, 1222); // Devuelve el vamor menor
result = Math.random(); // Devuelve un número aleatorio
result = Math.floor(Math.random() * 30); // Devuelve un número aleatorio multiplicado por 30

